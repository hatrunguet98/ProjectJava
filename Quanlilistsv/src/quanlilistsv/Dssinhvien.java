    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        int row = this.jTable1.getSelectedRow();
        String idRow = (this.jTable1.getModel().getValueAt(row, 0)).toString();
        sinhvien s1 =new sinhvien();
        s1.setName(txtname.getText());
        s1.setMsv(Integer.parseInt(txtmsv.getText()));
        s1.setAge(Integer.parseInt(txtage.getText()));
        new DAOsv().editSinhvien(s1,idRow);
        resartTb();
    }//GEN-LAST:event_jButton10ActionPerformed

    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        sinhvien s =new sinhvien();
        s.setName(txtname.getText());
        s.setMsv(Integer.parseInt(txtmsv.getText()));
        s.setAge(Integer.parseInt(txtage.getText()));
        new DAOsv().addSinhvien(s);
        list.add(s);
        showTable1();
        refreshJtxt();
        }
        public void showTable1() {
            sinhvien s = list.get(list.size() - 1);
            model.addRow(new Object[]{
                s.getId(),s.getMsv(),s.getName(),s.getAge()
            });
    }//GEN-LAST:event_jButton9ActionPerformed

