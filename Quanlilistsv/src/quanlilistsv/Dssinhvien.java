    public void showTable() {
        for (sinhvien s : list) {
            model.addRow(new Object[]{
                 s.getId(),s.getMsv(),s.getName(),s.getAge()
            });
        }
    }
    public void loadDb(){
        initComponents();
        list = new DAOsv().getListsinhvien();
        model = (DefaultTableModel) jTable1.getModel();
        model.setColumnIdentifiers(new Object[]{
            "id", "msv", "name", "age"
        });
        showTable();
    }
    public void loadDb(String id){
        initComponents();
        list = new DAOsv().getListsinhvien(id);
        model = (DefaultTableModel) jTable1.getModel();
        model.setColumnIdentifiers(new Object[]{
            "id", "msv", "name", "age"
        });
        showTable();
    }
    public void removeTb(){
        while(model.getRowCount() > 0)
        {
         model.removeRow(0);
        }
    }
    public void refreshJtxt(){
        txtmsv.setText("");
        txtname.setText("");
        txtage.setText("");
    }
    public void resartTb(){
        removeTb();
        list = new DAOsv().getListsinhvien();
        showTable();
        refreshJtxt();
    }
